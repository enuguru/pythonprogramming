
# A python program to create strings in three 
# different ways and concatenate them.
 
a = 'lovely' # string with single quotes
b = "flowers"   # string with double quotes
c = '''are
seen in hill
stations
all the time'''    # string with triple quotes
print(a)
print(b)
print(c)
 
# Concatenation of strings created using
# different quotes
print(a + b + c)
