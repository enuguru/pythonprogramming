
#Ascii_letters, lowercase, uppercase. The string module contains helpful constants. 
#We can use these to loop over all lowercase or uppercase ASCII letters, or to build 
#translation tables.

#Tip: We import the string module and access these constants on it. This is an easy to 
#way to access the constants.

#Python that uses ascii_letters constant

import string

# The letters constant is equal to lowercase + uppercase.
print(string.ascii_letters)
print(string.ascii_lowercase)
print(string.ascii_uppercase)
